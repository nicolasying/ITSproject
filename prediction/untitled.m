% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
b = str2num(get(handles.edit3,'String'));
global a;
global y;
for i=1:b
    te(i)=y(i+a-1);
end

global tmin;
global tmax;
%归一化
te=uniformization(te,tmin,tmax);

test=zeros(1,b-1);
resault=zeros(1,b-1);

  for i = 1:size(test,2) 
    test(1,i) = te(i);
    resault(1,i) = te(i + 1);
  end
  
 w_il= handles.w_il;
 w_hl= handles.w_hl;
 w_cl= handles.w_cl;
 w_if= handles.w_if;
 w_hf= handles.w_hf;
 w_cf= handles.w_cf;
 w_ic= handles.w_ic;
 w_hc= handles.w_hc;
 w_iw= handles.w_iw;
 w_hw= handles.w_hw;
 w_cw= handles.w_cw;
 w_ck= handles.w_ck;
 tmp_b_tc= handles.tmp_b_tc;
 tmp_s_tc= handles.tmp_s_tc;
%预测
[a_test_tl,b_test_tl,a_test_tf,b_test_tf,a_test_tc,s_test_tc,a_test_tw,b_test_tw,b_test_tc,a_test_tk,b_test_tk] = LSTM_Forecast( w_il,w_hl,w_cl,w_if,w_hf,w_cf,w_ic,w_hc,w_iw,w_hw,w_cw,w_ck,test,tmp_b_tc,tmp_s_tc);
p(1,:)=b_test_tk(1,1,:);
r(1,:)=resault(1,:);
% 反归一化
p(1,:)=p(1,:).*(tmax-tmin)+tmin;
r(1,:)=r(1,:).*(tmax-tmin)+tmin;

set(handles.edit11,'String',r(1,1));
set(handles.edit12,'String',r(1,6));
set(handles.edit13,'String',r(1,12));
set(handles.edit14,'String',r(1,24));

M=0;
for i=1:size(test,2)
M=M+(abs(r(1,i)-p(1,i))/r(1,i));
end
MAPE=(M*100)/size(test,2);

set(handles.edit6,'String',MAPE);

R=0;
for n=1:size(test,2)
R=R+(r(1,n) - p(1,n))^2;
end
RMSE=sqrt(R/size(test,2));

set(handles.edit8,'String',RMSE);

plot(handles.axes1,r,'color','blue');
hold;
plot(handles.axes1,p,'color','red');

guidata(hObject,handles);





